// ─── Context ─────────────────────────────────────────────────
const ThemeContext = createContext({
  themeId: 'monolith',
  theme: themes.monolith,
  setTheme: () => {},
  themeIds: Object.keys(themes),
});

export function useTheme() {
  return useContext(ThemeContext);
}

export const themeList = Object.entries(themes).map(([id, t]) => ({
  id,
  name: t.name,
  type: t.type,
  colors: t.colors,
}));

// ─── Provider ────────────────────────────────────────────────
const STORAGE_KEY = 'dax-theme';

export default function ThemeProvider({ children }) {
  const [themeId, setThemeId] = useState(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      if (saved && themes[saved]) return saved;
    } catch (_) {}
    return 'monolith';
  });

  const theme = themes[themeId] || themes.monolith;

  useEffect(() => {
    const root = document.documentElement;
    const colors = theme.colors;
    for (const [key, value] of Object.entries(colors)) {
      root.style.setProperty(`--dax-${key}`, value);
    }
    root.classList.toggle('theme-light', theme.type === 'light');
    root.classList.toggle('theme-dark', theme.type === 'dark');
  }, [theme]);

  const setTheme = useCallback((id) => {
    if (themes[id]) {
      setThemeId(id);
      try { localStorage.setItem(STORAGE_KEY, id); } catch (_) {}
    }
  }, []);

  useEffect(() => {
    const handler = (e) => setTheme(e.detail);
    document.addEventListener('dax-set-theme', handler);
    return () => document.removeEventListener('dax-set-theme', handler);
  }, [setTheme]);

  return (
    <ThemeContext.Provider value={{ themeId, theme, setTheme, themeIds: Object.keys(themes) }}>
      {children}
    </ThemeContext.Provider>
  );
}
